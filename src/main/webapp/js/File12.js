function showImg(dataset,i){

	var width = 1500*i;
	var height = 400;

	//在 body 里添加一个 SVG 画布   
	var svg = d3.select("#img")
	    .append("svg")
	    .attr("width", width)
	    .attr("height", height);

	//画布周边的空白
	 var padding = {left:30, right:30, top:20, bottom:20};

	 //x轴的比例尺
	 var xScale = /*d3.scale.ordinal()
	     .domain(d3.range(dataset.length))
	     .rangeRoundBands([0, 1440]);*/
		 d3.scale.linear()
	     .domain([1440*i,0])
	     .range([1440*i,0]);

	 //y轴的比例尺
	 var yScale = d3.scale.linear()
	     .domain([0,d3.max(dataset)])
	     .range([height - padding.top - padding.bottom, 0]);
	 
	//定义x轴
	 var xAxis = d3.svg.axis()
	     .scale(xScale)
	     .orient("bottom");

	 //定义y轴
	 var yAxis = d3.svg.axis()
	     .scale(yScale)
	     .orient("left");
	 
	//矩形之间的空白
	var rectPadding = 0;

	 //添加矩形元素
	 var rects = svg.selectAll(".MyRect")
	         .data(dataset)
	         .enter()
	         .append("rect")
	         .attr("class","MyRect")
	         .attr("transform","translate(" + padding.left + "," + padding.top + ")")
	         .attr("x", function(d,i){
	             return xScale(i);
	         } )
	         .attr("y",function(d){
	             return yScale(d);
	         })
	        /* .attr("width", xScale.rangeBand() - rectPadding )*/
	         .attr("width", '1px' )
	         .attr("height", function(d){
	             return height - padding.top - padding.bottom - yScale(d);
	         })
	         .attr("fill", "steelblue") ;

	 //添加文字元素
	/* var texts = svg.selectAll(".MyText")
	         .data(dataset)
	         .enter()
	         .append("text")
	         .attr("class","MyText")
	         .attr("transform","translate(" + padding.left + "," + padding.top + ")")
	         .attr("x", function(d,i){
	             return xScale(i) + rectPadding/2;
	         } )
	         .attr("y",function(d){
	             return yScale(d);
	         })
	         .attr("dx",function(){
	             return (xScale.rangeBand() - rectPadding)/2;
	         })
	         .attr("dy",function(d){
	             return 20;
	         })
	         .text(function(d){
	             return d;
	         });*/
	 
	//添加x轴
	 svg.append("g")
	   .attr("class","axis")
	   .attr("transform","translate(" + padding.left + "," + (height - padding.bottom) + ")")
	   .call(xAxis); 

	 //添加y轴
	 svg.append("g")
	   .attr("class","axis")
	   .attr("transform","translate(" + padding.left + "," + padding.top + ")")
	   .call(yAxis);
};



